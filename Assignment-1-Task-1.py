"""Takes two numbers as input from the user"""
num1=int(input("Enter the first number: "))
num2=int(input("Enter the second number: "))
"""Performs the basic mathematical operations on these two numbers:
o	Addition
o	Subtraction
o	Multiplication
o	Division
"""
numAdd = int(num1) + int(num2)
print ("Addition: ", numAdd)
numSub = int(num1) - int(num2)
print ("Subtraction: ", numSub)
numMult = int(num1) * int(num2)
print ("Multiplication: ", numMult)
numDiv = int(num1) / int(num2)
print ("Division: ", numDiv)
